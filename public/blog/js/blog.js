let currentCategory = 'all';
let currentTag = 'all';
let currentSearchTerm = '';

document.addEventListener('DOMContentLoaded', function () {
  initializeNavbar();
  initializeFilters();
  renderBlogPosts(blogPosts);
});

function initializeFilters() {
  const searchInput = document.getElementById('searchInput');
  const categoryFilters = document.getElementById('categoryFilters');
  const tagFilters = document.getElementById('tagFilters');

  if (categoryFilters) {
    const categories = ['all', ...new Set(blogPosts.map((post) => post.category))];
    categoryFilters.innerHTML = categories
      .map((category) => `<button class="filter-btn ${category === 'all' ? 'active' : ''}" data-type="category" data-value="${category}">${category === 'all' ? 'All Categories' : formatCategory(category)}</button>`)
      .join('');
  }

  if (tagFilters) {
    const tags = ['all', ...new Set(blogPosts.flatMap((post) => post.tags || []))];
    tagFilters.innerHTML = tags
      .map((tag) => `<button class="filter-btn filter-btn-tag ${tag === 'all' ? 'active' : ''}" data-type="tag" data-value="${tag}">${tag === 'all' ? 'All Tags' : '#' + tag}</button>`)
      .join('');
  }

  document.querySelectorAll('.filter-btn').forEach((button) => {
    button.addEventListener('click', function () {
      const type = this.dataset.type;
      const value = this.dataset.value;

      if (type === 'category') {
        document.querySelectorAll('.filter-btn[data-type="category"]').forEach((btn) => btn.classList.remove('active'));
        this.classList.add('active');
        currentCategory = value;
      }

      if (type === 'tag') {
        document.querySelectorAll('.filter-btn[data-type="tag"]').forEach((btn) => btn.classList.remove('active'));
        this.classList.add('active');
        currentTag = value;
      }

      filterAndRenderPosts();
    });
  });

  if (searchInput) {
    searchInput.addEventListener('input', function (e) {
      currentSearchTerm = e.target.value.toLowerCase().trim();
      filterAndRenderPosts();
    });
  }
}

function filterAndRenderPosts() {
  const filteredPosts = blogPosts.filter((post) => {
    const matchesCategory = currentCategory === 'all' || post.category === currentCategory;
    const matchesTag = currentTag === 'all' || (post.tags || []).includes(currentTag);
    const matchesSearch = !currentSearchTerm
      || post.title.toLowerCase().includes(currentSearchTerm)
      || post.excerpt.toLowerCase().includes(currentSearchTerm)
      || post.categoryLabel.toLowerCase().includes(currentSearchTerm)
      || (post.tags || []).some((tag) => tag.toLowerCase().includes(currentSearchTerm));

    return matchesCategory && matchesTag && matchesSearch;
  });

  renderBlogPosts(filteredPosts);
}

function renderBlogPosts(posts) {
  const blogGrid = document.getElementById('blogGrid');
  if (!blogGrid) return;

  blogGrid.innerHTML = '';

  if (!posts.length) {
    blogGrid.innerHTML = `
      <div style="text-align: center; padding: 3rem; color: var(--text-muted);">
        <i class="fas fa-search" style="font-size: 2.2rem; margin-bottom: 1rem; opacity: 0.55;"></i>
        <h3>No blog posts found</h3>
        <p>Try adjusting your search, category, or tag filters.</p>
      </div>
    `;
    return;
  }

  posts.forEach((post) => {
    const card = createBlogCard(post);
    blogGrid.appendChild(card);
  });
}

function createBlogCard(post) {
  const card = document.createElement('article');
  card.className = 'blog-card';
  card.onclick = () => navigateToBlogPost(post.slug);

  const tagsHtml = (post.tags || [])
    .map((tag) => `<span class="blog-card-tag">#${tag}</span>`)
    .join('');

  card.innerHTML = `
    <img src="${post.image}" alt="${post.title}" class="blog-card-image" loading="lazy">
    <div class="blog-card-content">
      <span class="blog-card-category">${post.categoryLabel}</span>
      <h3 class="blog-card-title">${post.title}</h3>
      <p class="blog-card-excerpt">${post.excerpt}</p>
      <div class="blog-card-tags">${tagsHtml}</div>
      <div class="blog-card-meta">
        <div class="blog-card-author">
          <i class="fas fa-user"></i>
          <span>${post.author}</span>
        </div>
        <div class="blog-card-date">
          <i class="fas fa-calendar"></i>
          <span>${formatDate(post.date)}</span>
        </div>
      </div>
      <div class="blog-card-read-more">
        <button class="read-more-btn" onclick="event.stopPropagation(); navigateToBlogPost('${post.slug}')">
          Read More <i class="fas fa-arrow-right"></i>
        </button>
      </div>
    </div>
  `;

  return card;
}

function formatCategory(category) {
  return category
    .split('-')
    .map((word) => word.charAt(0).toUpperCase() + word.slice(1))
    .join(' ');
}

function formatDate(dateString) {
  const options = { year: 'numeric', month: 'short', day: 'numeric' };
  return new Date(dateString).toLocaleDateString('en-US', options);
}

function navigateToBlogPost(slug) {
  window.location.href = '/blog/post/' + slug + '.html';
}

function initializeNavbar() {
  const navbar = document.querySelector('.navbar');
  const mobileMenuToggle = document.querySelector('.mobile-menu-toggle');
  const navbarMenu = document.querySelector('.navbar-menu');

  window.addEventListener('scroll', function () {
    if (!navbar) return;
    if (window.scrollY > 50) navbar.classList.add('scrolled');
    else navbar.classList.remove('scrolled');
  });

  if (mobileMenuToggle && navbarMenu && navbar) {
    mobileMenuToggle.addEventListener('click', function () {
      navbarMenu.classList.toggle('open');
      mobileMenuToggle.classList.toggle('open');
    });

    document.addEventListener('click', function (event) {
      if (!navbar.contains(event.target)) {
        navbarMenu.classList.remove('open');
        mobileMenuToggle.classList.remove('open');
      }
    });
  }
}
